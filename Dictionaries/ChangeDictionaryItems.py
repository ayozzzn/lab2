# Change Values.
# Change the "year" to 2018 :
thisdict = {
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}
thisdict["year"] = 2018

# Update Dictionary.
# The update() method will update the dictionary with the items from the given argument.
# Update the "year" of the car by using the update() method :
thisdict = {
  "brand": "Ford",
  "model": "Mustang",
  "year": 1964
}
thisdict.update({"year": 2020})